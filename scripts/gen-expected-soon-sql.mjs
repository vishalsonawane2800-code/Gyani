// One-off generator: read data/expected-soon-ipos/2026.csv and emit
// scripts/031_create_expected_soon_ipos.sql with CREATE TABLE + INSERTs.
//
// Re-run any time the CSV changes:
//   node scripts/gen-expected-soon-sql.mjs
//
// The output SQL is safe to apply via the Supabase SQL editor or psql:
//   - creates the table only if missing
//   - enables RLS with a public-read policy (matches other IPO tables)
//   - truncates + re-seeds expected_soon_ipos so the table mirrors the CSV

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

const csvPath = path.join(ROOT, 'data/expected-soon-ipos/2026.csv');
const outPath = path.join(ROOT, 'scripts/031_create_expected_soon_ipos.sql');

// Minimal RFC4180 CSV parser (handles quoted fields with embedded commas).
function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = '';
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          cell += '"';
          i++;
        } else {
          inQuotes = false;
        }
      } else {
        cell += c;
      }
    } else if (c === '"') {
      inQuotes = true;
    } else if (c === ',') {
      row.push(cell);
      cell = '';
    } else if (c === '\n') {
      row.push(cell);
      rows.push(row);
      row = [];
      cell = '';
    } else if (c === '\r') {
      // skip
    } else {
      cell += c;
    }
  }
  if (cell.length > 0 || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }
  return rows;
}

function sqlString(v) {
  if (v == null) return 'NULL';
  const s = String(v).trim();
  if (!s) return 'NULL';
  return `'${s.replace(/'/g, "''")}'`;
}

const text = fs.readFileSync(csvPath, 'utf8');
const rows = parseCsv(text);
const header = rows[0].map((h) => h.trim());
const dataRows = rows.slice(1).filter((r) => r.length > 1 && (r[0] || '').trim());

const colIndex = (name) => header.indexOf(name);
const C = {
  company: colIndex('Company'),
  offerType: colIndex('Offer Type'),
  saleType: colIndex('Sale Type'),
  drhp: colIndex('DRHP Filing Date'),
  sebi: colIndex('SEBI Approval Date'),
  size: colIndex('Estimated Issue Size'),
  exchange: colIndex('Exchange'),
  lead: colIndex('Primary Lead Manager'),
  industry: colIndex('Industry'),
};

const lines = [];
lines.push('-- Auto-generated by scripts/gen-expected-soon-sql.mjs');
lines.push('-- Source: data/expected-soon-ipos/2026.csv');
lines.push('-- Run on the new Supabase project to seed the expected-soon IPO pipeline.');
lines.push('');
lines.push('CREATE TABLE IF NOT EXISTS expected_soon_ipos (');
lines.push('  id                    SERIAL PRIMARY KEY,');
lines.push('  company               TEXT NOT NULL,');
lines.push('  offer_type            TEXT,');
lines.push('  sale_type             TEXT,');
lines.push('  drhp_filing_date      TEXT,        -- raw "DD-Mon-YYYY" string from SEBI');
lines.push('  sebi_approval_date    TEXT,        -- raw "DD-Mon-YYYY" string, NULL if not approved');
lines.push('  estimated_issue_size  TEXT,');
lines.push('  exchange              TEXT,');
lines.push('  primary_lead_manager  TEXT,');
lines.push('  industry              TEXT,');
lines.push('  created_at            TIMESTAMPTZ DEFAULT NOW(),');
lines.push('  UNIQUE (company)');
lines.push(');');
lines.push('');
lines.push('CREATE INDEX IF NOT EXISTS idx_expected_soon_ipos_industry ON expected_soon_ipos(industry);');
lines.push('CREATE INDEX IF NOT EXISTS idx_expected_soon_ipos_exchange ON expected_soon_ipos(exchange);');
lines.push('CREATE INDEX IF NOT EXISTS idx_expected_soon_ipos_drhp_date ON expected_soon_ipos(drhp_filing_date);');
lines.push('');
lines.push('ALTER TABLE expected_soon_ipos ENABLE ROW LEVEL SECURITY;');
lines.push('');
lines.push('-- Public read (matches the listed_ipos / ipos public-read policy).');
lines.push('DROP POLICY IF EXISTS "Allow public read on expected_soon_ipos" ON expected_soon_ipos;');
lines.push('CREATE POLICY "Allow public read on expected_soon_ipos"');
lines.push('  ON expected_soon_ipos FOR SELECT USING (true);');
lines.push('');
lines.push('-- Reset and reseed the table so it mirrors the CSV exactly.');
lines.push('TRUNCATE TABLE expected_soon_ipos RESTART IDENTITY;');
lines.push('');
lines.push('INSERT INTO expected_soon_ipos (');
lines.push('  company, offer_type, sale_type, drhp_filing_date, sebi_approval_date,');
lines.push('  estimated_issue_size, exchange, primary_lead_manager, industry');
lines.push(') VALUES');

const valueLines = [];
for (const r of dataRows) {
  const cells = [
    r[C.company],
    r[C.offerType],
    r[C.saleType],
    r[C.drhp],
    r[C.sebi],
    r[C.size],
    r[C.exchange],
    r[C.lead],
    r[C.industry],
  ].map(sqlString);
  valueLines.push(`  (${cells.join(', ')})`);
}
lines.push(valueLines.join(',\n') + ';');
lines.push('');
lines.push(`-- Inserted ${dataRows.length} rows.`);
lines.push('');

fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`[gen] wrote ${outPath} (${dataRows.length} rows)`);
